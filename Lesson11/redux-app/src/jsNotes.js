//if else

var number = 10;

if (number === 1) {
    console.log("Number is 1");
}
else if (number === 2) {
    console.log("Number is 2");
}
else if (number === 3) {
    console.log("Number is 3");
}
else if (number === 4) {
    console.log("Number is 4");
}
else if (number === 5) {
    console.log("Number is 5");
}
else {
    console.log("Number is not 1,2,3,4,5");
}



//switch case

var number = 10;

switch (number) {
    case 1: console.log("Number is 1");break;
    case 2: console.log("Number is 2");break;
    case 3: console.log("Number is 3");break;
    case 4: console.log("Number is 4");break;
    case 5: console.log("Number is 5");break;
default: console.log("Number is not 1,2,3,4,5");
}
